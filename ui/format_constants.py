BK = "\033[30m"    # Black
R = "\033[31m"     # Red
G = "\033[32m"     # Green
Y = "\033[33m"     # Yellow
BU = "\033[34m"    # Blue
M = "\033[35m"     # Magenta
C = "\033[36m"     # Cyan
W = "\033[37m"     # White
DE = "\033[39m"    # Default

BGBK = "\033[40m"    # Background Black
BGR = "\033[41m"     # Background Red
BGG = "\033[42m"     # Background Green
BGY = "\033[43m"     # Background Yellow
BGBU = "\033[44m"    # Background Blue
BGM = "\033[45m"     # Background Magenta
BGC = "\033[46m"     # Background Cyan
BGW = "\033[47m"     # Background White
BGD = "\033[39"      # Background Default

RE = "\033[0m"     # Reset

B = '\033[1m'   # Bold
D = '\033[2m'   # Dim
I = '\033[3m'   # Italic
U = '\033[4m'   # Underline
F = '\033[5m'   # Flash
V = '\033[7m'   # Inverse
H = '\033[8m'   # Hidden
S = '\033[9m'   # Strikethrough

UL = '\033[F'  # Move cursor up to previous line (Up Line)
NL = '\n'      # New line
